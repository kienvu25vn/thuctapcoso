class Stack {

    int top = -1;
    int size = 0;
    int[] arr;

    public Stack(int size) {
        arr = new int[size];
        this.size = size;
    }

    public void push(int value) {
        arr[++top] = value;
    }

    public void pop() {
        top--;
    }

    public int peek() {
        if (top > -1) {
            return arr[top];
        }

        return -1;
    }
    public int size() {
        return top + 1;
    }
}

