    private void btnPushActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPushActionPerformed
        // TODO add your handling code here:
        int Push = -1;
        try {
            Push = Integer.parseInt(pushTxt.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid number!");
        }

        if (stack.top == -1 && Push != -1) {
            txt1.setText(Push + "");
        } else if (stack.top == 0 && Push != -1) {
            txt2.setText(txt1.getText());
            txt1.setText(Push + "");
        } else if (stack.top == 1 && Push != -1) {
            txt3.setText(txt2.getText());
            txt2.setText(txt1.getText());
            txt1.setText(Push + "");
        } else if (stack.top == 2 && Push != -1) {
            txt4.setText(txt3.getText());
            txt3.setText(txt2.getText());
            txt2.setText(txt1.getText());
            txt1.setText(Push + "");
        } else if (stack.top == 3 && Push != -1) {
            txt5.setText(txt4.getText());
            txt4.setText(txt3.getText());
            txt3.setText(txt2.getText());
            txt2.setText(txt1.getText());
            txt1.setText(Push + "");
        }

        if (stack.top <= 3) {
            stack.push(Push);
        }


    }//GEN-LAST:event_btnPushActionPerformed

